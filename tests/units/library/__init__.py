    'data-ref-id="3010-2618">Computer and Video Games</a>,</i> '
    "issue 174, May 1996, pages 106-111), former British <a "
    'title="Industrial Light &amp; Magic (page does not exist)" '
    'href="/industrial-light-magic/3010-7050/" '

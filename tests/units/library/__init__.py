    'title="Hironobu Sakaguchi" '
    'href="/hironobu-sakaguchi/3040-633/" '
    'data-ref-id="3040-633">Hironobu Sakaguchi</a> <a '
    'href="https://archive.org/stream/Computer_and_Video_Games_Issue_191_1997-10_EMAP_Images_GB#page/n53/mode/2up" '

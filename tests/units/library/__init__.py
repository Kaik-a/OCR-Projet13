    "Lies</i> (see <a "
    'href="https://archive.org/stream/Computer_and_Video_Games_Issue_185_1997-04_EMAP_Images_GB#page/n87/mode/2up" '
    'rel="nofollow" data-ref-id="false">here</a>). <i>Final '
    'Fantasy VII</i> was the <a title="List of most expensive '
    'video games to develop" '

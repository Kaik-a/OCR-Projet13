    'href="/paul-ashdown/3040-455/" data-ref-id="3040-455">Paul '
    'Ashdown</a> who worked on <i><a title="Star Wars" '
    'href="/star-wars/3025-551/" data-ref-id="3025-551">Star '
    'Wars</a></i> and <i><a title="Jurassic Park" '

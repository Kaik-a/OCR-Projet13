    'central theme of "life" dates back to when his mother passed '
    'away while he was working on <a title="Final Fantasy III" '
    'href="/final-fantasy-iii/3030-21463/" '
    'data-ref-id="3030-21463">Final Fantasy III</a>, after which '
    'he always wanted to explore the theme of "life" in a '
    '"mathematical and logical way" maybe "to overcome the mental '
    'shock" but it was not until Final Fantasy VII that he '
    'explored the theme of "life" for the first time in the '
    "series.</p><p>Soon after the release of <a "
    'href="../../final-fantasy-vi/61-6336/" '
    'rel="nofollow">FFVI</a>, Square started pre-production on '
    "FFVII in 1994. The game was originally planned as a 2D game "
    "on the SNES, that was not the only major change to how the "
    "game eventually turned out. The Creator of Final Fantasy, <a "
    'href="../../hironobu-sakaguchi/72-633/" '
    'rel="nofollow">Hironobu Sakaguchi</a> originally intended for '
    "the game to take place in present day New York City. "
    "Development on the game was stalled, due to priority going "
    'over to the <a href="../../chrono-trigger/61-9249/" '
    'rel="nofollow">Chrono Trigger</a> project that was being '
    "developed at the same time.</p><p>Development recommenced "
    "later on in 1995. The original script saw a major overview, "
    "with a detective story theme being cast aside and "
    'co-director, <a href="../../yoshinori-kitase/72-634/" '
    'rel="nofollow">Yoshinori Kitase</a>, pushed for the game to '
    "go 3D for concerns that the series would be left behind back "
    "in the 2D era. Also, it was decided to make the game for "
    "Nintendos next console, the N64. Though due to FFVIIs "
    "ambitious design, Square expressed concerns to Nintendo over "
    "their decision to use cartridges instead of a new format "
    "capable of storing much more information, called CD-ROMs, on "
    "their new console. This dispute continued and led to Square "
    "announcing on the 12 of January 1996, that it would be "
    "dropping relations with Nintendo and developing FFVII on "
    "Sonys debut console, the Playstation. A reported $45,000,000 "
    "was spent over the extent of the games "
    "development.</p><p>Development required the efforts of "
    "approximately 120 artists and programmers, using "
    "PowerAnimator and Softimage 3D software. This was the <a "
    'href="https://archive.org/stream/Computer_and_Video_Games_Issue_191_1997-10_EMAP_Images_GB#page/n51/mode/2up" '
    'rel="nofollow" data-ref-id="false">largest game development '
    "team</a> at the time, and included Japanese CG artists "
    'working alongside <a title="Cinema of the United States (page '
    'does not exist)" href="/hollywood/3035-642/" '
    'data-ref-id="3035-642">Hollywood</a> CG <a title="Visual '
    'effects (page does not exist)" '
    'href="http://gaming.wikia.com/wiki/Visual_effects?action=edit&amp;redlink=1" '
    'rel="nofollow">visual effects</a> artists, such as Ron '
    'Sabatino (see <i><a title="Computer and Video Games (page '
